/**
 * Output writer — assembles all pipeline artifacts into file content strings
 * and writes them to disk.
 *
 * buildOutputs()  → pure function, returns all five artifact strings
 * writeOutputs()  → the only function with side effects (fs.writeFile calls)
 *
 * The business.ts template is the most complex output: it must produce valid
 * TypeScript that satisfies the Business interface in @/types/index.ts, using
 * the real Lucide icon components (not the string names used internally in the
 * pipeline). String icon names like 'ShieldCheck' are converted to import
 * references in the generated file.
 */

import { writeFile, mkdir } from 'fs/promises';
import { join } from 'path';

import type {
  PipelineResult,
  OutputFiles,
  PipelineProofPoint,
  PipelineBenefit,
  PipelineServiceEntry,
  PipelineFAQItem,
  PipelineStat,
  ConfidenceReport,
  ReviewQueueItem,
  DesignBrief,
  PhotoBrief,
  PhotoShot,
} from '../types';

// ─── Public API ───────────────────────────────────────────────────────────────

/**
 * Pure function: build all five output artifacts from a pipeline result.
 * Returns strings ready to write to disk — no I/O.
 */
export function buildOutputs(result: PipelineResult): OutputFiles {
  return {
    'business.ts': generateBusinessTs(result),
    '_confidence.json': generateConfidenceJson(result.confidenceReport),
    '_review_queue.md': generateReviewQueueMd(result.reviewQueue),
    '_design_brief.md': generateDesignBriefMd(result.designBrief, result.rawData.name),
    '_photo_brief.md': generatePhotoBriefMd(result.photoBrief, result.rawData.name),
  };
}

/**
 * Write all five output files to the client's data directory.
 *
 * @param outputs   - From buildOutputs()
 * @param clientSlug - e.g. 'martinez-landscaping'
 * @param rootDir   - Project root, defaults to process.cwd()
 */
export async function writeOutputs(
  outputs: OutputFiles,
  clientSlug: string,
  rootDir: string = process.cwd(),
): Promise<void> {
  const dir = join(rootDir, 'src', 'data', 'businesses', clientSlug);
  await mkdir(dir, { recursive: true });

  await Promise.all(
    (Object.entries(outputs) as Array<[keyof OutputFiles, string]>).map(([filename, content]) =>
      writeFile(join(dir, filename), content, 'utf-8'),
    ),
  );
}

