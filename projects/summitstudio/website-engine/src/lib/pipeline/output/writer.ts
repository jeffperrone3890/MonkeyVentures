// ─── Formatting helpers ───────────────────────────────────────────────────────

/** Single-quote-safe JSON string */
function q(s: string): string {
  return JSON.stringify(s);
}

function toSlug(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '');
}

function formatHours(hours?: Array<{ day: string; time: string }>): string {
  if (!hours || hours.length === 0) {
    return [
      "    { day: 'Monday–Friday', time: '7:00 AM – 6:00 PM' },\n",
      "    { day: 'Saturday', time: '8:00 AM – 4:00 PM' },\n",
      "    { day: 'Sunday', time: 'Closed' },\n",
    ].join('');
  }
  return hours.map((h) => `    { day: ${q(h.day)}, time: ${q(h.time)} },\n`).join('');
}

function formatProofPoints(points: PipelineProofPoint[]): string {
  return points
    .map(
      (p) =>
        `    { icon: ${p.iconName}, label: ${q(p.label)}, detail: ${q(p.detail)}${p.computed ? `, computed: ${q(p.computed)}` : ''} },\n`,
    )
    .join('');
}

function formatServices(services: PipelineServiceEntry[]): string {
  return services
    .map(
      (s) =>
        `    {\n` +
        `      slug: ${q(s.slug)},\n` +
        `      title: ${q(s.title)},\n` +
        (s.displayTitle ? `      displayTitle: ${q(s.displayTitle)},\n` : '') +
        `      summary: ${q(s.summary)},\n` +
        `      details: ${q(s.details)},\n` +
        `      includes: [\n` +
        s.includes.map((inc) => `        ${q(inc)},\n`).join('') +
        `      ],\n` +
        `      icon: ${s.iconName},\n` +
        `    },\n`,
    )
    .join('');
}

function formatBenefits(benefits: PipelineBenefit[]): string {
  return benefits
    .map(
      (b) =>
        `    { icon: ${b.iconName}, title: ${q(b.title)}, description: ${q(b.description)} },\n`,
    )
    .join('');
}

function formatStats(stats: PipelineStat[]): string {
  return stats.map((s) => `    { value: ${q(s.value)}, label: ${q(s.label)} },\n`).join('');
}

function formatTestimonials(
  testimonials?: Array<{
    quote: string;
    author?: string;
    location?: string;
    rating?: number;
    service?: string;
  }>,
): string {
  if (!testimonials || testimonials.length === 0) return '';
  return testimonials
    .map(
      (t) =>
        `    {\n` +
        `      quote: ${q(t.quote)},\n` +
        `      author: ${q(t.author ?? '')},\n` +
        `      location: ${q(t.location ?? '')},\n` +
        `      rating: ${t.rating ?? 5},\n` +
        (t.service ? `      service: ${q(t.service)},\n` : '') +
        `    },\n`,
    )
    .join('');
}

function formatFAQ(faq: PipelineFAQItem[]): string {
  return faq
    .map((f) => `    { question: ${q(f.question)}, answer: ${q(f.answer)} },\n`)
    .join('');
}

// ─── Confidence JSON ──────────────────────────────────────────────────────────

function generateConfidenceJson(report: ConfidenceReport): string {
  const summary = {
    generatedAt: new Date().toISOString(),
    totalFields: Object.keys(report).length,
    flaggedFields: Object.values(report).filter((e) => e.flagged).length,
    averageScore:
      Object.values(report).reduce((sum, e) => sum + e.score, 0) / Object.keys(report).length,
    fields: report,
  };
  return JSON.stringify(summary, null, 2);
}

// ─── Review queue markdown ────────────────────────────────────────────────────

function generateReviewQueueMd(queue: ReviewQueueItem[]): string {
  if (queue.length === 0) {
    return `# Review Queue\n\nAll fields passed confidence threshold. No review required.\n`;
  }

  const mustRewrite = queue.filter((i) => i.severity === 'must-rewrite');
  const shouldVerify = queue.filter((i) => i.severity === 'should-verify');
  const quickConfirm = queue.filter((i) => i.severity === 'quick-confirm');

  const formatItem = (item: ReviewQueueItem): string => {
    const lines = [
      `### \`${item.field}\``,
      `**Score:** ${(item.score * 100).toFixed(0)}% · **Source:** ${item.source}`,
      '',
      `**Current value:**`,
      '```',
      typeof item.currentValue === 'string'
        ? item.currentValue
        : JSON.stringify(item.currentValue, null, 2),
      '```',
    ];
    if (item.hint) {
      lines.push('', `**Hint:** ${item.hint}`);
    }
    if (item.alternatives && item.alternatives.length > 0) {
      lines.push('', '**Alternatives:**');
      (item.alternatives as string[]).forEach((alt, i) => {
        lines.push(`${i + 1}. ${typeof alt === 'string' ? alt : JSON.stringify(alt)}`);
      });
    }
    return lines.join('\n');
  };

  const sections: string[] = [
    `# Review Queue`,
    ``,
    `Generated: ${new Date().toISOString().split('T')[0]}`,
    ``,
    `**${mustRewrite.length} must-rewrite · ${shouldVerify.length} should-verify · ${quickConfirm.length} quick-confirm**`,
    ``,
    `---`,
  ];

  if (mustRewrite.length > 0) {
    sections.push(``, `## Must Rewrite`, `*Score below 40% — AI had insufficient context. Replace with accurate content.*`, ``);
    sections.push(...mustRewrite.map(formatItem));
  }

  if (shouldVerify.length > 0) {
    sections.push(``, `## Should Verify`, `*Score 40–59% — AI-generated from limited signals. Verify facts before publishing.*`, ``);
    sections.push(...shouldVerify.map(formatItem));
  }

  if (quickConfirm.length > 0) {
    sections.push(``, `## Quick Confirm`, `*Score 60–69% — Likely accurate but worth a second read. Tweak wording if needed.*`, ``);
    sections.push(...quickConfirm.map(formatItem));
  }

  return sections.join('\n');
}

// ─── Design brief markdown ────────────────────────────────────────────────────

